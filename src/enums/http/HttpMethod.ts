// src/enums/http/HttpMethods.ts

/**
 * Enum representing common HTTP methods.
 * This enum provides a type-safe way to refer to HTTP methods used in requests.
 */
export enum HttpMethods {
    GET = 'GET',
    POST = 'POST',
    PUT = 'PUT',
    DELETE = 'DELETE',
    PATCH = 'PATCH',
    OPTIONS = 'OPTIONS',
    HEAD = 'HEAD',
    TRACE = 'TRACE',
    CONNECT = 'CONNECT'
}
